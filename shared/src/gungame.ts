function fx(partial: WeaponFx): WeaponFx {
  return partial;
}

type GgPartial = Omit<
  WeaponDef,
  "id" | "name" | "adsSpreadMult" | "adsFov" | "adsSensMult" | "scopeStyle"
> &
  Partial<
    Pick<
      WeaponDef,
      | "adsSpreadMult"
      | "adsFov"
      | "adsSensMult"
      | "scopeStyle"
      | "maxRange"
      | "meleeCone"
      | "fx"
      | "viewmodel"
      | "semiAuto"
      | "explosionRadius"
      | "projectile"
      | "onHit"
      | "onHitMs"
    >
  >;

function gg(id: string, name: string, partial: GgPartial): WeaponDef {
  return {
    id,
    name,
    adsSpreadMult: partial.adsSpreadMult ?? 0.4,
    adsFov: partial.adsFov ?? 58,
    adsSensMult: partial.adsSensMult ?? 0.75,
    scopeStyle: partial.scopeStyle ?? "iron",
    maxRange: partial.maxRange,
    meleeCone: partial.meleeCone,
    fx: partial.fx,
    viewmodel: partial.viewmodel,
    semiAuto: partial.semiAuto,
    explosionRadius: partial.explosionRadius,
    projectile: partial.projectile,
    onHit: partial.onHit,
    onHitMs: partial.onHitMs,
    damage: partial.damage,
    headshotMultiplier: partial.headshotMultiplier,
    magSize: partial.magSize,
    reloadMs: partial.reloadMs,
    fireRate: partial.fireRate,
    pellets: partial.pellets,
    spreadDeg: partial.spreadDeg,
    recoilPattern: partial.recoilPattern,
  };
}

